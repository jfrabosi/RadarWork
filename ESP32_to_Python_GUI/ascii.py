def ascii_to_printf(ascii_art):
    printf_lines = []
    for line in ascii_art.split('\n'):
        # Escape backslashes and double quotes
        escaped_line = line.replace('\\', '\\\\').replace('"', '\\"')
        # Replace % with %% to escape it for printf
        escaped_line = escaped_line.replace('%', '%%')
        printf_lines.append(f'printf("{escaped_line}\\n");')
        printf_lines.append('HAL_Delay(25);')
    return '\n'.join(printf_lines)

